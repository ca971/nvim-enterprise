--- ║  │  └─ config function           → replaces core config entirely    │    ║
--- ║  │                                                                  │    ║
--- ║  │  The default user uses all core plugins unchanged.               │    ║
--- ║  │  This file returns an empty list — no extra specs.               │    ║
--- ║  │                                                                  │    ║
