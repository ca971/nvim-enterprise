--- ║  │  │    { "tpope/vim-fugitive", cmd = { "Git" } }                  │    ║
--- ║  │  ├─ Override a core plugin's opts:                               │    ║
--- ║  │  │    { "nvim-telescope/telescope.nvim",                         │    ║
